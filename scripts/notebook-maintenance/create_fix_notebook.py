    "        if isinstance(tensor, torch.Tensor) and tensor.dtype in (torch.float16, torch.float32):",
    "            if tensor.numel() > 1:  # Skip scalars",
    "                fp16_tensors.append((key, tensor.half() if tensor.dtype == torch.float32 else tensor))",
    "",
    "    print(f'Extracted {len(fp16_tensors)} tensors from state dict')",
    "",
    "del state  # Free memory",
