    "",
    "# Check format",
    "version = state.get('version', 'unknown')",
    "print(f'Format version: {version}')",
    "",
    "if 'layers' in state or 'version' in state:",
    "    # Compressed format (save_fabqrc_compressed output)",
    "    print('Detected compressed format, reconstructing FP16...')",
    "    ",
    "    codebook = state.get('codebook')",
    "    allocation = state.get('allocation', {})",
    "    blocksize_results = state.get('blocksize_results', {})",
    "    layers = state.get('layers', {})",
    "    ",
    "    fp16_tensors = []",
    "    quantized_count = 0",
    "    for layer_name, ldata in layers.items():",
    "        quantized_count += 1",
    "        out_c = ldata['original_out_features']",
    "        in_c = ldata['original_in_features']",
    "        weight = torch.zeros(out_c, in_c, dtype=torch.float16)",
    "",
    "        # Reconstruct int8 channels",
    "        if len(ldata.get('int8_channels', [])) > 0:",
    "            ch = ldata['int8_channels'].long()",
